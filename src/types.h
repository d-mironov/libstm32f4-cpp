#pragma once
#include <stdint.h>

/**
 * Unsigned 8-bit integer
 */
typedef uint8_t u8;

/**
 * Unsigned 16-bit integer
 */
typedef uint16_t u16;

/**
 * Unsigned 32-bit integer
 */
typedef uint32_t u32;

/**
 * Unsigned 64-bit integer
 */
typedef uint64_t u64;

/**
 * Signed 8-bit integer
 */
typedef int8_t i8;

/**
 * Signed 16-bit integer
 */
typedef int16_t i16;

/**
 * Signed 32-bit integer
 */
typedef int32_t i32;

/**
 * Signed 64-bit integer
 */
typedef int64_t i64;

/**
 * 32-bit floating point number
 */
typedef float f32;

/**
 * 64-bit floating point number
 */
typedef double f64;

typedef char* string;

/**
 * Pointer to unsigned 8-bit integer
 * Can also be an array of 8-bit integers
 */
typedef uint8_t* u8p;

/**
 * Pointer to unsigned 16-bit integer
 * Can also be an array of 16-bit integers
 */
typedef uint16_t* u16p;

/**
 * Pointer to unsigned 32-bit integer
 * Can also be an array of 32-bit integers
 */
typedef uint32_t* u32p;

/**
 * Pointer to unsigned 64-bit integer
 * Can also be an array of 64-bit integers
 */
typedef uint64_t* u64p;

/**
 * Pointer to signed 8-bit integer
 * Can also be an array of 8-bit integers
 */
typedef int8_t* i8p;

/**
 * Pointer to signed 16-bit integer
 * Can also be an array of 16-bit integers
 */
typedef int16_t* i16p;

/**
 * Pointer to signed 32-bit integer
 * Can also be an array of 32-bit integers
 */
typedef int32_t* i32p;

/**
 * Pointer to signed 64-bit integer
 * Can also be an array of 64-bit integers
 */
typedef int64_t* i64p;
